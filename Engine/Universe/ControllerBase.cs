using System;
using System.Collections.Generic;

namespace GLSharp.Universe {
    public abstract class ControllerBase {
        public Node Parent;

        //------------------------------------------------------------------------------------------
        //------------------------------------------------------------------------------------------
        public void Bind(Node parent) {
            this.Parent = parent;
            this.BindParent();
        }
        //------------------------------------------------------------------------------------------
        //------------------------------------------------------------------------------------------
        protected abstract void BindParent();
        //------------------------------------------------------------------------------------------
        //------------------------------------------------------------------------------------------
        public abstract void Update(float delta);
    }
}
